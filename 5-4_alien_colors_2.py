# version that passes the if statement:
alien_color = 'green'
if alien_color == 'green':
    print('You have earned 5 points!')
elif alien_color != 'green':
    print('You just earned 10 points!')

# version that doesn't pass the if statement:
alien_color = 'red'
if alien_color == 'green':
    print('You have earned 5 points!')
elif alien_color != 'green':
    print('You just earned 10 points!')